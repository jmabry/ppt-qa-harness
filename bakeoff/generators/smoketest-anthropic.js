const pptxgen = require("pptxgenjs");

const pres = new pptxgen();
pres.layout = "LAYOUT_16x9";
pres.title = "Smoke Test";

const slide = pres.addSlide();

// Dark blue background
slide.background = { color: "1E2761" };

// White centered title "Hello World"
slide.addText("Hello World", {
  x: 0.5,
  y: 1.8,
  w: 9,
  h: 1.2,
  fontSize: 54,
  fontFace: "Calibri",
  color: "FFFFFF",
  bold: true,
  align: "center",
  valign: "middle",
});

// Subtitle below
slide.addText("Generated by pptxgenjs", {
  x: 0.5,
  y: 3.2,
  w: 9,
  h: 0.8,
  fontSize: 24,
  fontFace: "Calibri",
  color: "CADCFC",
  align: "center",
  valign: "middle",
});

pres.writeFile({ fileName: "outputs/smoketest-anthropic.pptx" }).then(() => {
  console.log("Saved: outputs/smoketest-anthropic.pptx");
});
